 
¿qué significa que?
 
ISR = qué interrupciones se están ejecutando
 S => Servicio
IRR = qué interrupciones están pedidas
 R => Request (pedido)
 
ISR =  08h = 0000 1000 => La subrutina de la interrupcion 3 (CMDA) se está ejecutando
IRR =  01h = 0000 0001 => la subrutina de la interrupcion 0 (f10) está esperando ser ejecutada



